package com.tibco.cep.dashboard.psvr.mal;

public class MALException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7941085620604043304L;

	public MALException() {
	}

	public MALException(String message) {
		super(message);
	}

	public MALException(Throwable cause) {
		super(cause);
	}

	public MALException(String message, Throwable cause) {
		super(message, cause);
	}

}
