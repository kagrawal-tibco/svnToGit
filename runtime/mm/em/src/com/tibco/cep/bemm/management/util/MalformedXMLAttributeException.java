public class MalformedXMLAttributeException extends Exception {

    public MalformedXMLAttributeException() {
        super();
    }

    public MalformedXMLAttributeException(String message) {
        super(message);
    }

    public MalformedXMLAttributeException(String message, Throwable cause) {
        super(message, cause);
    }

    public MalformedXMLAttributeException(Throwable cause) {
        super(cause);
    }
}
