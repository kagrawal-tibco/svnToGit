package com.tibco.cep.studio.wizard.as.services.exception;

public class InvalidSharedResourceException extends Exception {

	public InvalidSharedResourceException() {
	}

	public InvalidSharedResourceException(String message) {
		super(message);
	}

	public InvalidSharedResourceException(Throwable cause) {
		super(cause);
	}

	public InvalidSharedResourceException(String message, Throwable cause) {
		super(message, cause);
	}

}
