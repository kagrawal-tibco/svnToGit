package com.tibco.cep.studio.wizard.as.commons.beans.factory.exception;

public class CanNotCreateBeanException extends RuntimeException
{

	public CanNotCreateBeanException()
	{
		super();
	}

	public CanNotCreateBeanException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public CanNotCreateBeanException(String message)
	{
		super(message);
	}

	public CanNotCreateBeanException(Throwable cause)
	{
		super(cause);
	}

}
