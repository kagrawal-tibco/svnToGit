	public static enum IMAGE_SIZE{
		SIZE_16("16x16"),
		SIZE_24("24x24"),
		SIZE_32("32x32"),
		SIZE_48("48x48");

		private static final IMAGE_SIZE[] VALUES_ARRAY =
				new IMAGE_SIZE[] {
			SIZE_16,
			SIZE_24,
			SIZE_32, 
			SIZE_48
		};

		private final String value;
		int width;
		int height;

		private IMAGE_SIZE(final String value) {
			this.value = value;
			String size[] = value.split("x");
			width = Integer.valueOf(size[0]);
			height = Integer.valueOf(size[1]);
		}

		public int getHeight() {
			return height;
		}

		public int getWidth() {
			return width;
		}

		public String toString() {
			return this.value;
		}

		public static IMAGE_SIZE get(String literal) {
			for (int i = 0; i < VALUES_ARRAY.length; ++i) {
				IMAGE_SIZE result = VALUES_ARRAY[i];
				if (result.toString().equals(literal)) {
					return result;
				}
			}
			return null;
		}

	};

	public String getImagePath(String key) {
		return getImagePath(key, IMAGE_SIZE.SIZE_16);
	}

	public String getImagePath(String key, IMAGE_SIZE size) {

		String imgPath = "";
		if(key == null || key.isEmpty()){
			switch(size) {
			case SIZE_16: 
				imgPath = imagePathRegistry16x16.get(DEFAULT_NULL_ICON);
				break;
			case SIZE_24: 
				imgPath = imagePathRegistry24x24.get(DEFAULT_NULL_ICON);
				break;
			case SIZE_32: 
				imgPath = imagePathRegistry32x32.get(DEFAULT_NULL_ICON);
				break;
			case SIZE_48:
				imgPath = imagePathRegistry64x64.get(DEFAULT_NULL_ICON);
				break;
			}
		} else {
			switch(size) {
			case SIZE_16: 
				if (imagePathRegistry16x16.get(key) != null){
					imgPath = imagePathRegistry16x16.get(key);
				} else {
					imgPath = imagePathRegistry16x16.get(DEFAULT_NULL_ICON);
				}
				break;
			case SIZE_24: 
				if (imagePathRegistry24x24.get(key) != null) {
					imgPath = imagePathRegistry24x24.get(key);
				} else {
					imgPath = imagePathRegistry24x24.get(DEFAULT_NULL_ICON);
				}
				break;
			case SIZE_32: 
				if (imagePathRegistry32x32.get(key) != null) {
					imgPath = imagePathRegistry32x32.get(key);
				} else { 
					imgPath = imagePathRegistry32x32.get(DEFAULT_NULL_ICON);
				}
				break;
			case SIZE_48: 
				if (imagePathRegistry64x64.get(key) != null) {
					imgPath = imagePathRegistry64x64.get(key);
				} else {
					imgPath = imagePathRegistry64x64.get(DEFAULT_NULL_ICON);
				}
				break;
			}
		}

		return imgPath;
	}

	public boolean loadImageMap(String key, String path) {

		if(path.contains(IMAGE_SIZE.SIZE_16.toString())){ 
			imagePathRegistry16x16.put(key, path);
		}
		else if(path.contains(IMAGE_SIZE.SIZE_24.toString())){
			imagePathRegistry24x24.put(key, path);
		}
		else if(path.contains(IMAGE_SIZE.SIZE_32.toString())){
			imagePathRegistry32x32.put(key, path);
		}
		else if(path.contains(IMAGE_SIZE.SIZE_48.toString())){
			imagePathRegistry64x64.put(key, path);
		}
		else{
			imagePathRegistry16x16.put(key, path);
		}

		return true;
	}

